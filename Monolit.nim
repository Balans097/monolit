# src/ffmpeg_api нужен здесь только ради av_log_set_level — настраиваем
# уровень лога FFmpeg ниже, до передачи управления в src/gui.
import src/[gui, ffmpeg_api]

proc main() =
  # Полная, но НЕ избыточная диагностика: AV_LOG_INFO показывает ключевое
  # (сведения о потоках/кодеках, финальную сводку x264 — QP/битрейт/типы
  # кадров, предупреждения и ошибки), но не сыплет построчным логом
  # КАЖДОГО записанного блока mkv-мьюксера (это уровень AV_LOG_DEBUG —
  # слишком подробно для повседневной диагностики). FFmpeg пишет лог в
  # stderr, поэтому он появится прямо в терминале, из которого запущено
  # приложение.
  av_log_set_level(AV_LOG_INFO)
  let status = runApp()
  if status != 0:
    quit(status)

when isMainModule:
  main()
